package com.greatbuild.clearcost.msvc.msvcchange.models.dtos;

/**
 * DTO para recibir datos de miembro de proyecto desde msvc-projects
 */
public class ProjectMemberDTO {

    private Long id;
    private Long userId;
    private String role;        // COORDINATOR, SPECIALIST
    private String specialty;   // Para SPECIALIST
    private String email;
    private String firstName;
    private String lastName;

    // Constructors
    public ProjectMemberDTO() {
    }

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getSpecialty() {
        return specialty;
    }

    public void setSpecialty(String specialty) {
        this.specialty = specialty;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
}
